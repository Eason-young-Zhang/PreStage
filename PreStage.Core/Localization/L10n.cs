    public static string Tr(string key)
    {
        if (_currentLanguage == "zh" && ChineseStrings.TryGetValue(key, out var zh))
            return zh;
        if (_currentLanguage == "de" && GermanStrings.TryGetValue(key, out var de))
            return de;
        return key;
    }

    public static void SetLanguage(string lang)
    {
        if (lang?.StartsWith("zh", StringComparison.OrdinalIgnoreCase) == true)
            _currentLanguage = "zh";
        else if (lang?.StartsWith("de", StringComparison.OrdinalIgnoreCase) == true)
            _currentLanguage = "de";
        else
            _currentLanguage = "en";
    }

